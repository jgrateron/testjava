package com.fresco;

public class Documentation {

	/**
	 * sum of two integers
	 * 
	 * @param a the first value
	 * @param b the second value
	 * @return the result
	 * @author jairo
	 * @since 2024-10-14
	 */
	public static int sum(int a, int b) {
		return a + b;
	}

	public static void main(String[] args) {
		sum(1, 2);
	}
}


